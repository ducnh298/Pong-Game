    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(cp==0){
            cp=1;
            jPasswordField1.setEditable(true);
            jPasswordField2.setEditable(true);
            jPasswordField3.setEditable(true); 
            jPasswordField1.requestFocus();
        }
        else if(cp==1){
            if(jPasswordField1.getText().equals(""))
                JOptionPane.showMessageDialog(this, "Please enter your old password!");
            else if(jPasswordField1.getText().equals(user.getPassword())){
                if(jPasswordField2.getText().equals(jPasswordField3.getText())){
                    User newUser = user;
                    newUser.setPassword(jPasswordField2.getText());
                    myControl.sendData(new ObjectWrapper(ObjectWrapper.CHANGE_PASSWORD, newUser));
                }
                else
                    JOptionPane.showMessageDialog(this, "New password do not match!");
            }
            else
                JOptionPane.showMessageDialog(this, "Password incorrect!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

