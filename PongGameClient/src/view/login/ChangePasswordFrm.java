    public void receivedData(ObjectWrapper data){
        if(data.getPerformative()==ObjectWrapper.REPLY_USER){
            if(data.getData() instanceof User){
                this.user = (User) data.getData();
                jLabel1.setText("Nick name: "+player.getNickName());
                jLabel2.setText("User name: "+user.getUsername());
            }
            else
                JOptionPane.showMessageDialog(this, "Failed to get user info from database!");
        }
        if(data.getPerformative()==ObjectWrapper.REPLY_CHANGE_PASSWORD){
            if(data.getData().equals("ok")){
                JOptionPane.showMessageDialog(this, "Password changed successfully!");
                cp=0;
                jPasswordField1.setText("");
                jPasswordField2.setText("");
                jPasswordField3.setText("");
                
                jPasswordField1.setEditable(false);
                jPasswordField2.setEditable(false);
                jPasswordField3.setEditable(false);
                myControl.sendData(new ObjectWrapper(ObjectWrapper.GET_USER, player.getId()));
            }
            else 
                JOptionPane.showMessageDialog(this, "Password changed unsuccessfully!");
        }
    }
