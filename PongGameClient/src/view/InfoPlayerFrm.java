    void initPlayer(Player player){
        if(mainPlayer.getId()==player.getId())
            jButton1.setEnabled(false);
        else jButton1.setEnabled(true);
        jTextField1.setText(player.getId()+"");
        jTextField2.setText(player.getNickName());
        jTextField3.setText(player.getStatus()+"");
        jTextField4.setText(player.getMatchWon()+"");
        jTextField5.setText(player.getMatchPlayed()+"");
        if(player.getMatchPlayed()!=0)
            jTextField6.setText(player.getMatchWon()*100/player.getMatchPlayed()+"%");
        else
            jTextField6.setText("0%");
        
        for(int i=0;i<friendList.size();i++){
            if(player.getId()==friendList.get(i).getId()){
                jButton1.setEnabled(false);
                break;
            }
        }
        if(!player.getStatus().equals("Online")||mainPlayer.getId()==player.getId())
            jButton2.setEnabled(false);
        else jButton2.setEnabled(true);
    }
    
