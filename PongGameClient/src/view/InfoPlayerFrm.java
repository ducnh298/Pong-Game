    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
                    
            int choice=JOptionPane.showConfirmDialog(this, "Are you sure want to challenge this player", "Challenge", JOptionPane.YES_NO_OPTION);
            if(choice==JOptionPane.YES_OPTION){
                ArrayList<Integer> challenge = new ArrayList<>();
                challenge.add(mainPlayer.getId());
                challenge.add(player.getId());
                myControl.sendData(new ObjectWrapper(ObjectWrapper.CHALLENGE, challenge));
                JOptionPane.showMessageDialog(this, "Challenge request sent!\nPlease wait a moment for response!");
                Message ms = new Message(mainPlayer.getId(), player.getId(),"challenge");
                myControl.sendData(new ObjectWrapper(ObjectWrapper.MESSAGE_PLAYER, ms));
                this.dispose();
            }
        
       
    }//GEN-LAST:event_jButton2ActionPerformed
    public void receivedData(ObjectWrapper data){
        
        if(data.getPerformative()==ObjectWrapper.REPLY_REQUEST_FRIEND){
            int result = (int) data.getData();
            if(result==1){
                JOptionPane.showMessageDialog(this, "You've already sent this player a friend request.");
                jButton1.setEnabled(false);
            }
            else if(result==2){
                JOptionPane.showMessageDialog(this, "Friend Request sent!");
            }
            else 
                JOptionPane.showMessageDialog(this, "Failed to send friend request!");
            }
       if(data.getPerformative()==ObjectWrapper.REPLY_UNFRIEND){
                if(data.getData().equals("ok")){
                    JOptionPane.showMessageDialog(this,"Unfriend successfully!");
                    jButton1.setEnabled(true);
                    jButton3.setEnabled(false);
                    if(ff!=null){
                        ff.updateUnFriend(player);
                    }
                    setDefaultCloseOperation(javax.swing.WindowConstants.DISPOSE_ON_CLOSE);   
                    this.dispose();
                }
                else 
                    JOptionPane.showMessageDialog(this, "Unfriend failed!");
            }
    }
